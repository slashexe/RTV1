void	ft_striteri(char *s, void (*f) (unsigned int, char *))
{
	unsigned int cpt;

	cpt = 0;
	if (!s || !f)
		return ;
	while (*s)
	{
		f(cpt, s);
		s++;
		cpt++;
	}
}
