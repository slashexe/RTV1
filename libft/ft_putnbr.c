#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		if (n != -2147483648)
		{
			ft_putchar('-');
			n *= -1;
		}
		else
		{
			ft_putchar('-');
			ft_putchar('2');
			n = 147483648;
		}
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		n = n % 10;
	}
	ft_putchar(n + '0');
}
